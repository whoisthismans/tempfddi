							<h3 align="center"> First prize won by FDDI, Hyderabad student during ALT- International Knowledge Fest - Virtual Crosslinx ’21 </h3></br>

						<p align="justify">  A student of Footwear Design & Development Institute (FDDI), Hyderabad participated in ALT- International Knowledge Fest - Virtual Crosslinx ’21,  in one of the event category i.e., Product Designing Contest (PDC) and won the first place.</p>
						<p align="justify">  Ms. Lakshmi Gayatri Pudipeddi, a design elective student from FDDI School of Footwear Design & Production (FSFDP) showcased her creativity & talent and won the first place in the PDC.</p>
						<p align="justify">  In an attempt to charge the young minds with the passion to innovate in leather and allied fields, the Association of Leather Technologists (ALT) with support of Center Leather Research Institute (CLRI), Chennai organized the fest under the guidance of Dr. K. J. Sreeram, Director, CSIR-CLRI.</p>
						<p align="justify">  The contest aims at the process of innovative creation of a new product. It provided the most effective and efficient path towards the development of innovative ideas for new products.</p>
						<p align="justify">  The PDC had two rounds. Initially, the students of B. Des - FDP (2018- 22 Batch) submitted their ideas as an abstract format. Based on the abstract, three students namely, Ms. Lakshmi Gayatri Pudipeddi, Ms. Sameeksha Ambolikar and Ms. Mohini Mohanty were shortlisted for the final round. </p>
						<p align="justify">  The final round was a technical round that included a mood board, story-telling concept, idea sketches, hand drawing or CAD rendering and tech packs and so on.</p>
						<p align="justify">  During the final round, Ms. Lakshmi Gayatri Pudipeddi depicted and explained her work on ‘Qalamklo’ which solely focused on blending one of the traditional folk art forms, such as Kalamkari with contemporary design. These heritage heirlooms are accentuated on modern boot components made of leather with wooden sole. </p>
																								

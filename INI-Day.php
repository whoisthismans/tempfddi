							<h3 align="center"> ‘INI Day’ celebrated at FDDI, Noida campus </h3>

						<p align="justify"> On 5th August 2023, Footwear Design & Development Institute (FDDI) celebrated ‘INI Day’ at its Noida campus.  </p>
						<p align="justify"> As FDDI was declared “Institute of National Importance” vide Government of India gazette Notification No. 30, Ministry of Law & Justice (Legislative Department), New Delhi dated 5th August 2017, this date, i.e.  5th August has been considered for celebrating ‘INI Day (Enactment)’ of FDDI. </p>

							<h3 align="center"> FDDI participates in ‘India Skills Competition 2021 – Regionals Shoe & Garment Making’ as ‘Venue’ sponsor </h3>

						<p align="justify"> Footwear Design & Development Institute (FDDI) participated in ‘India Skills Competition 2021 – Regionals Shoe & Garment Making’ as ‘Venue’ sponsor.</p>
						<p align="justify"> National Skill Development Corporation (NSDC) organized the competition in partnership with Sector Skill Councils, Industry Partners, Corporates and Academic Organizations.</p>
						<p align="justify"> India Skills 2021 provides a platform for skilled and talented Indian youngsters to showcase their abilities at regional and national level competitions in over 50 skills. The regional levels of the competitions are organized across four zones, culminating at the national competition. The winners of the India Skills competition get an opportunity to participate in the World Skills competition, known as the ‘Olympics of Skills’.</p>
						<p align="justify"> In the India Skills 2021 edition, Leather sector is represented as a demo trade (Leather is not part of World Skills 2021 edition). Under leather, two competition categories were held namely, Shoe Making and Garment Making.</p>
						<p align="justify"> For the leather sector, Leather Sector Skill Council (LSSC) invited applications from eligible participants to showcase their talent, win prizes and national recognition by hosting the event region wise at the selected venue.</p>
																								

						<p align="justify"> “As far as strategy for the future is concerned, in my opinion, to improve the growth rate, some industry clusters should be developed on the modern method of manufacturing technology which is supported with adequate skilled manpower. Design and Retail information must be regularly made available to leather & footwear manufacturers to help them suitably address the season's requirement. Besides this, Technology Upgradation; Market Access; Intellectual Property Rights and Patents; Incubators and Entrepreneurship Development; Quality Management Standards are needed,” added, Mr. Sinha.  </p>

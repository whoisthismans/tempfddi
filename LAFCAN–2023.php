							<h3 align="center"> FDDI participates in LAFCAN – 2023  </h3>

						<p align="justify"> Footwear Design & Development Institute (FDDI) participated in ‘Leather Accessories Footwear Conclave & Awards of North India’ (LAFCAN-2023) “Vision 2030 - Strategies to Stimulate Growth in Leather and Footwear Sector of India”, which was organized by the Council for Leather Exports (CLE)) on 26th April, 2023 at ITC Maurya, New Delhi.  </p>
						<p align="justify"> On this occasion, Mr. Sanjay Leekha, Chairman, CLE, Mr. R. Selvam, Executive Director, CLE, Mr. Santosh Sarangi, Director General, DGFT, Govt. of India, Mr. Rajeev Singh Thakur, Additional Secretary, Department for Promotion of Industry and Internal Trade (DPIIT), Govt. of India, Mr. Motilal Sethi, Northern Regional Chairman, CLE, Mr. Arun Kumar Sinha, Managing Director (MD), FDDI, Mr. Pankaj Kumar Sinha, Secretary-FDDI, Mr. Sunil Sethi, Chairman, Fashion Design Council of India, Senior Govt. officials from Ministry, Buying Groups, Industry Experts were present.  </p>
						<p align="justify"> The conclave took off with the lightening of the inaugural lamp by the dignitaries.   </p>
						<p align="justify"> The conclave, a significant networking platform was organized to motivate and guide MSME exporters of CLE to achieve the ambitious target of USD 47 bn by 2030.  </p>
						<p align="justify"> There was a ‘Panel Discussion’ on the ‘Role of 7S of Industry to achieve the ambitious target of USD 47 bn by 2030’ during which the stalwarts from Industry, Academia and Government belonging to the domain of 7S i.e. Skill, Scale, Speed, Sustainability, Supply Chain, Style and Sales presented their views. </p>
						<p align="justify"> Sharing his views during the plenary session of LAFCAN-2023, on the topic ‘Skilling Requirement in Leather & Footwear Industry of India’, Mr. Arun Kumar Sinha, IAS, MD, FDDI said, “Fulfilling the goals of Hon’ble Prime Minister’s ‘Vocal for Local’, ‘Make in India’, ‘Aatmanirbhar Bharat’, ‘Skill India’, and ‘Start-up India’ Programmes and thus, achieving the ambitious target of USD 47 Billion by 2030, the need of the hour is to adhere to the 7S which are interrelated factors for which the Government’s pro-MSME’s policy and innovative approach paves way to achieve the target.”  </p>
																											

							<h3 align="center"> FDDI, Hyderabad campus in collaboration with LEPRA Society organizes technical workshop on ‘Footwear with Orthosis for Deformed Feet’  </h3>

						<p align="justify"> On 26th July 2022, Footwear Design & Development Institute (FDDI), Hyderabad campus in collaboration with LEPRA Society organized a technical workshop on ‘Footwear with Orthosis for Deformed Feet’. </p>
						<p align="justify"> Under the supervision and guidance of Design faculty of FDDI School of Footwear Design & Production (FSFDP), the students of B.Des FDP (2021-2025) were taken to LEPRA Society located at New Nallakunta zone, Hyderabad.  </p>
						<p align="justify"> LEPRA Society is a non-governmental organization that promotes quality health care, initiates and fosters new developments and implementation. It aims to support the National Health Programmes in the prevention and control of diseases such as Leprosy, Lymphatic Filariasis and other neglected diseases. </p>
						<p align="justify"> Its New Nallakunta zone has been actively working since 1988, to help people with leprosy, people with diabetes mellitus and so on. It is also having a shoe manufacturing unit, where they are making footwear with orthoses for people in need. </p>						
												
						<span class="news-details-img">
						<img class="img-responsive" src="img/new@fddi/kameswara-rao.JPG" alt="img">						
						</span>
						
						<p align="justify"> The morning session of the workshop was conducted by Mr. Kameswara Rao, Physiotherapist & Project Officer, LEPRA Society. He is trained from Central Leprosy Teaching and Research Institute (CLTRI) having more than 33 years of experience in LEPRA Society and has also published several research papers in various international reputed journal. </p>
						<p align="justify"> Mr. Kameswara Rao briefed about the normal physiology of human body & feet, affected patients with anesthetic feet, symptoms and treatment. He further briefed about the relationship between foot type, foot deformity and effectiveness of footwear with orthosis for deformed foot. He also gave an in-depth knowledge of human foot anatomy, functions of feet, importance of Gait cycle for affected feet with various diseases and parameters to be considered for designing of footwear with orthosis. In addition, he explained benefits of Micro Cellulose Rubber (MCR) as shock absorption, capacity of shore hardness, high pressure points and prevention of foot ulcer. </p>
						<p align="justify"> The afternoon session was conducted by Mr. Kameswara Rao and Mr. Devdas. </p>
						<p align="justify"> Mr. Devdas is an artisan-cum-shoe technician having more than 30 years of experience. During this session, the participants received hands-on practice which helped them to acquire good understanding of feet measurement system for deformed feet, support of orthosis pad for supinated, pronated feet and hallux limitus.  </p>
						<p align="justify"> The technical workshop was organized with the objective to provide active learning experiences and exposure to the practical tools and skills needed to thrive in professional endeavors. </p>
																											

						<p align="justify"> The students visited the Paragon-B footwear unit, which is spread over 16 acres of land and has a large non- leather manufacturing area (upper & assembly). The students were first given a vivid tour of Unit B of the factory, which produces 33,000 pairs per day. After watching various sequence of operation in the unit, the students were led to the quality section, where the soles were tested for flexibility and the final footwear was tested for suitability of fitting, using a last. Furthermore, they were exposed to various sole testing methods. </p>
						<p align="justify"> In the second half, the students were taken to the Paragon unit A having EVA Hawai chapel production unit, with a capacity of 50,000 pairs per day. First, students were taken to the raw material mixing section where they learned about involvement of recycled material (acquired from cutting waste) as one of the major ingredients which, reduced the overall common cost involved in mixing. Further, they witnessed the whole extrusion and sheet building process, and were provided every detail about it. </p>
						<p align="justify"> The students also had an interaction session with the HR, Production Manager, Designer and other front-office executives where their queries were clarified.  During the interaction session, Mr. Sreejith Nair, General Manager, Hyderabad Division & his team briefed about quality parameters of safety shoes, career opportunities in non- leather sector, Kaizen Quality Management and Lean Manufacturing, etc. </p>
																		

						<p align="justify"> With an objective to provide an insight of the working environment of the organization and also to increase the technical knowledge of the students of Footwear Design & Development Institute (FDDI), Hyderabad campus, an industrial visit was organized on 24th September 2022 at Paragon Polymers Products Private Limited. </p>
						<p align="justify"> Under the supervision and guidance of faculty of School of Footwear Design & Production (SFDP), the students of B.Des FDP 2020 & 2021 Batches visited Unit B & Unit A of Paragon Polymers Products Private Limited located in Patancheru, Hyderabad.  </p>
												

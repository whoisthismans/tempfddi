						<p>The students of Footwear Design & Development Institute (FDDI), Kolkata visited ‘Sabala Mela’ on 3rd December 2019 which was held at Central Park Salt Lake, Kolkata.</p>
						<p>Under the guidance of faculty of FDDI, the students of 2018 Batch, Semester 3 and Session 2019 - 2020 visited ‘Sabala Mela’ which was a part of awareness and promotion activity about the local crafts and knowledge enhancement.</p>
						<p>This annual fair is organised by West Bengal Swarojgar Corporation Limited, Govt. of West Bengal Undertaking, Self-Help Group and Self-Employment Department (SHG and SE) is a dedicated hub for visitors who witness artisans live as they manufacture handicraft items. The fair provides a platform to SHGs and small-scale entrepreneurs to exhibit and sell their goods.</p>
						<p>There were over 300 stalls and two Karigari Haats in the Mela. The products that were sold include baluchari, jamdani, zardozi and boutique sarees, terracotta items of Bankura, including Dokra crafts of Bishnupur, burnt-clay home décor items, leather bags, artificial flowers, zari work items, orchids, bamboo and cane items, waxworks, shola items, muslin items, tulaipanji rice, Chhau masks from Purulia, mats from Medinipur, and various other handicraft items.</p>
						<p>The visit helped the students to enhance their professional skills by studying the diverse and rich art & craft heritage of the country and interacting with the crafts persons from all over India. </p>


						
                        
						



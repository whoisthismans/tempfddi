						<p align="justify"> Under the educational trip, the B. Des. FDP (2018-22) - Design elective specialization students of Footwear Design & Development Institute (FDDI), Hyderabad campus visited the ‘Salar Jung Museum’ in the city of Hyderabad on 14th October 2021. </p>

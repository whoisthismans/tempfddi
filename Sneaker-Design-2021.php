						<p align="justify"> Mr. Vidit Singh who pursued Footwear & Apparel Innovation Design at Pensole Footwear Design Academy based in Portland, Oregon, USA and is currently a Product Design student at College for Creative Studies, Detroit, Michigan, USA, conducted the workshop.</p>
																								

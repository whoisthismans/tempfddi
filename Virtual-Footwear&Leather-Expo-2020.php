						<p align="justify"> Addressing during the launch of the event, Mr. Arun Kumar Sinha, IAS, Managing Director, FDDI said, “As visualized by our Hon’ble Prime Minister, we are looking forward to ‘Atma Nirbhar Bharat’ and there is a dire need to transform India into a global manufacturing hub that not only caters to own requirements, but also of the world. I hope through this event, FDDI and FICCI will provide a platform to Footwear & Leather Goods industry to highlight India as a global manufacturing hub. This event will also assist us to move towards the knowledge-based economy.”</p>
						<p align="justify"> The Virtual Footwear and Leather Expo 2020 will be the 1st Virtual Expo in India on Footwear and Leather sector. The Expo will have on virtual display the entire range of products relating to Footwear and leather Industry including their ancillaries. Key Components of the event will be Exhibition (Pavilions and Showrooms), B2B (International, Domestic, and Institutional), Webinars & Digital Sessions on each Showcased Sector, Instant Interaction platforms Live Chats, Chat bots and Global Footprint (International Buyers).</p>					
						<p align="justify"> Mr. Dilip Chenoy, Secretary General, FICCI  said “Footwear and Leather sector is one of the priority sectors that has been identified by the Government to not only increase its value addition within India, but also to increase the export potential. Virtual Footwear and Leather Expo 2020 will act as a key tool for achieving these significant goals.” </p>						

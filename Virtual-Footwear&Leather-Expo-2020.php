							<h3 align="center"> FDDI & FICCI officially launch ‘Virtual Footwear and Leather Expo 2020’  </h3></br>

						<p align="justify"> Footwear & Design Development Institute (FDDI) in association with Federation of Indian Chambers of Commerce and Industry (FICCI),  is organizing the 1st edition of  ‘Virtual Footwear and Leather Expo 2020’  from 30th November 2020 to 6th December 2020 to  promote Leather and Footwear Industry.</p>
						<p align="justify"> The official launch of this event was held on Friday, 9th October, 2020 virtually by Mr. Arun Kumar Sinha, IAS, Managing Director, FDDI and Mr. Dilip Chenoy, Secretary General, FICCI which was attended by approximately 190 participants.</p>												
												

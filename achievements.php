                        <h4><span class="icon-ic-icon-12"></span> Consultancy (Domestic & International)</h4>
                        <p align="justify">FDDI provides consultancy for development of quality systems at both domestic and international levels to the Footwear, Leather and allied industry.</p>
						<p align="justify">Further to the successful completion of the Twinning Program (Phase-1) during 2011-14, FDDI (along with CLRI) had entered into another agreement of “Twinning Programme (Phase-2)” in May 2015 for two (02) years with the Ministry of Industry, Federal Democratic Republic of Ethiopia represented by Leather Industry Development Institute (LIDI), Ethiopia. </p>
                        <p align="justify">Under the New agreement of Twinning (Phase-2) the following program have been undertaken by FDDI in addition to the ongoing B.Sc. program with Addis Ababa Science & Technology University (AASTU) Addis Ababa. The B.Sc. program is now self-sustained at LIDI-AASTU:</p>
                        <ul class="achivements-ul">
                            <li>a.  Establishment and conduct of training at Technical and Vocational Education & Training (TVET) Centers in Ethiopia.</li>
                            <li>b.  Industrial Consultancy in Footwear Technology.</li>
                            <li>c.  Long term & short term training program at FDDI, India.</li>
                        </ul>
                        <p align="justify">The second phase of the Twinning -2 was further extended till December 2018 & additional activity pertaining to `Non Leather Sector- survey and preparation of Road map’ was allocated to FDDI.</p>
						<p align="justify">FDDI has further enhanced the cooperation with the LIDI by signing `Extension of Twinning Agreement phase 2’ from 2020 till 2025. The extended phase focuses on the development of the consultancy services, non leather sector and augmenting higher learnings. The present phase is expected bring much needed development process for the institutional development.</p>

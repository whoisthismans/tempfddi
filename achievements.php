                    <div class="achivements-box">
                        <h4><span class="icon-ic-icon-14"></span> Research & Development</h4>
                        <p align="justify">FDDI has developed customized solutions for its various clients in the area of Footwear Technology.</p>
						<p align="justify">Being an apex Institute for footwear, the Institute has always been a trendsetter and has made remarkable progress by developing methodologies in footwear research.</p>
						<p align="justify">FDDI, under the ‘Make in India – 2.0’ (MII) Programme, has been mandated by the Department of Commerce to develop Standards for Footwear & Accessories for Armed Forces & Paramilitary Forces, through its International Testing Center (ITC). In this regard, as per the ‘Action Plan’, FDDI alongwith Bureau of Indian Standards (BIS) and Armed Forces/Paramilitary Forces is working for up-gradation & development of standards.</p>
						<p align="justify">Under this, the Up-gradation/Development of Standards for Tactical Boot (High Ankle) with PU-Rubber Sole for Paramilitary Forces, Anti-Riot Shoes with PU-Rubber Sole for Paramilitary Forces (RAF), Shoes Derby Black with PU-Rubber Sole for State Police Department Full Rubber Knee/Ankle Boot for Paramilitary Forces & Light Weight, Anti-Skid, Anti Penetration Combat Boot for Indian Army have been carried–out by the Footwear Sectional Committee (CHD-19): Panel-1, of BIS.</p>
						<p align="justify">Further, a New Standard (IS-17012:2018) - "High Ankle Tactical Boots with PU-Rubber Sole" has been published by BIS in September 2018 under MII-2.0.</p>

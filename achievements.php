                        <p><span class="icon-ic-icon-13"></span> <strong> Development & Operationalization of Online Web Portal of the IDLS Scheme</strong></p>
                        <p align="justify">FDDI and CLRI are the two arms of DIPP working as Project Implementation Unit (PIU) for the Integrated Development of Leather Sector (IDLS) scheme for the period of 3 years (2017-20) for Tanneries and Product units respectively.</p>
						<p align="justify">For timely execution of the Scheme, the Online web portal of IDLS Scheme has been unveiled on 28th March 2018 at FDDI, Noida. This portal provides an overall view of the Online Application System right from application submission till disbursement of funds & monitoring.  The tools available in the Online Application system enables to completely automate the administration & processing of online repository of various applicant unit.</p>
						<p align="justify">The new online application and tracking system of IDLS would simplify the process and cut down the time lag during implementation substantially. </P

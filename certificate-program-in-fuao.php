                                <h4>Certificate Program in Footwear Upper Assembly Operator </h4>                                
								<p align="justify"> The Prime objective of this program is to equip the learners with the identified knowledge and skills required as stitching operators in the field of the upper assembly department. Learners are therefore expected to attain the basic skill of upper closing and assembly of the uppers. The second element of the proposed course curriculum is the continuous assessment process throughout the training program. After cutting the components in the cutting department they come for stitching in the upper assembly department. In this department, components are stitched with the help of various types of main stitching machines and ancillary machines like flat-bed single & double needle machine, post bed single & double needle machine, zigzag machine, skiving machine, thermo-folding machine etc. So the main objective of this program is to train learners on these machines and have to understand the technical aspects of making an upper with the help of different types of needles and threads. </p>
								<p align="justify"> The Objective of the training program in footwear is to develop the capacities of learners to understand technical aspects of upper closing. The candidate will have basic knowledge about the Closing department and operations. The skillset requirement covering relevant knowledge components are addressed in the course curriculum. The curriculum has been designed on the principle of KSA (Knowledge – Skill- Attitude) based learning emphasizing the mastering of the skills & knowledge through the self-assessment process.</p>								

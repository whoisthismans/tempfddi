					<h4>Detailed Information on Hazardous Banned Chemicals Substances:</h4><br>
					<p><b>AZO DYE</b></p>
					<p align="justify">The azo dye test is to be determine the values of amines, especially which are a carcinogen. Different colourants including dyes and pigments are used for the colouration of fashion articles such as apparel, textile, footwear and accessories.</p>
					<p><b>CHROMIUM</b></p>
					<p align="justify">Chromium is a naturally-occurring element found in rocks, animals, plants and soil. It is widely used in manufacturing processes and can be found in many consumer products such as wood treated with copper dichromate, leather tanned with chromic sulfate, and stainless steel cookware, Chromium can be found in air, soil and water after release from the manufacture, use and disposal of chromium-based products and during the manufacturing process.</p>
					<p align="justify">Chromium (VI) compounds are more toxic than chromium(III) compounds. The most common health problem resulting from exposure to chromium involves the respiratory tract, these health effects include irritation of the lining of the nose, runny nose and breathing problems such as asthma, cough , shortness of breath, wheezing.</p>
					<p align="justify"><b>PENTACHLOROPHENOL (PCP) </b></p>
					<p align="justify">Pentachlorophenol (PCP) can be used to prevent fungal growth and decay by bacteria. In textile and leather industry, PCP is mostly used for preservation finishing; in ligneous products. PCP can be used in conservation paints. PCP can also be used in wooden toys and handicrafts.</p>
					<p align="justify">PCP and its salts are highly toxic for aquatic systems, harmful to human health and highly persistent in the environment. Products containing PCP may form highly toxic substances when they are incinerated.</p>
					<p><b>FORMALDEHYDE</b></p>
					<p align="justify">Although formaldehyde is an incredibly useful chemical, there are known ecological and toxicological risks associated with exposure and handling, particularly in industrial and laboratory-based environments. In concentrated forms formaldhyde is an irritant and a highly reactive, flammable and toxic substance that should be handled in controlled manner.</p>
					<p align="justify">Therefore, formaldehyde testing is necessary for products compliance due to the chemical being restricted globally according to the material type, product and end consumer (i.e adult nor child, in skin contact etc) with the acceptable limits for children being significant lower than those for adults.</p>
				
				

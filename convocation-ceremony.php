							<h3 align="center"> Smt. Anupriya Patel, Hon’ble MoS, C&I, GoI graces the ‘Convocation ceremony’ at FDDI, NOIDA campus; inaugurates the ‘Digital Classroom’ </h3>

						<p align="justify"> Amid maintaining precautionary measures of COVID-19 pandemic, Footwear Design & Development Institute (FDDI), NOIDA held its Convocation ceremony for the passing out students of the Session 2020 & 2021 on 27th December 2021.</p>
						<p align="justify"> The event was graced by Smt. Anupriya Patel, Hon’ble Minister of State (MoS), Commerce and Industry (C&I), Government of India (GoI) as the Chief Guest, whereas the other distinguished guests of the event who graced the occasion were Mr. Motilal Sethi, Managing Director, M/s. Saroj International, President – Indian Leather Garments Association (ILGA) and Mr. Sanjay Gupta,  Director, M/s. Sandeep Rubber, President - Indian Footwear Components Manufacturers Association (IFCOMA).</p>
						<p align="justify"> On behalf of FDDI, Mr. Arun Kumar Sinha, IAS, Managing Director (MD), FDDI welcomed the Chief Guest and all the other delegates including person from the print and electronic media during which the staff & students were also present.</p>
						<p align="justify"> Smt. Anupriya Patel, Hon’ble MoS – C&I, GoI also inaugurated the newly setup ‘Digital Classroom’ facility which has been replicated to all other eleven campuses of FDDI.</p>
																														

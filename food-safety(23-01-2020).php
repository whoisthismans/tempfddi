                            <h5>CHEMICAL LABORATORY</h5>
                            <p>The Chemical Laboratory is well equipped with all the modern instruments / techniques of Chemical Analysis.</p>
                            <p>TEST RANGE &amp; MATERIALS:</p>
                            <ul>
                                <li>Chemical Testing Materials such as - Leather, Textiles and Polymers;</li>
                                <li>Testing of Banned Materials such as - AZO dyes, Trichlorophneol, Tetrachlorophenol, Pentachlorophenol, Formaldehyde, Chromium-6, Chromium Trioxide, Heavy Metal Detection, Dispersed Dyes, pH and pH of Water Soluble, Ash Content, Oil &amp; Fat Content, Phthalates, Water Soluble Content, RoHS.</li>

                            <p>INSTRUMENTS:</p>
                            <ul>
                                <li>UV- Visible Spectrophotometer: This is used for analysis of compounds having double-triple bonds and functional groups such as OH, COOH, COCH3 etc.</li>
                                <li>HPLC (High Performance Liquid Chromatography): This is a very versatile analytical tool used for determination of amines and Formaldehyde (HCHO).</li>
                                <li>GC-ECD &amp; FID (Gas Chromatography coupled with Electron Capture Detector and Flame Ionisation Detector): The ECD is selective towards compounds with electronegative groups such as chlorinated phenols and Biphenyls and FID is used to check purity of solvents and VOC's (Residual Solvents).</li>
                                <li>FTIR (Fourier Transform Infra Red Spectroscopy): This is used as quick analytical tool for identification of chemicals, powders and solvents. This special ATR device is widely used for the identification of Plastics and polymers.</li>
                                <li>ICP-MS (Inductively Coupled Plasma with Mass Spectrometer): This is widely used for the detection/estimation of heavy metals such as Lead, Cadmium, Mercury, Arsenic etc, to a very low level to extent of ppb/ppt.</li>
                                <li>LC-MS (Liquid Chromatograph coupled with Mass Selective Detector): This is used for analysis of residual pesticides, disperse and allergenic dyes, etc.</li>
                                <li>GC-MS (Gas Chromatograph coupled with Mass Selective Detector): This is used for determination of pesticides, phthalates, DMF etc.</li>

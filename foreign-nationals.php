                        <p align="justify"> <h2 style="font-size:14px;">NON RESIDENT INDIAN (NRI)</h2></p>
						<p align="justify">Non Resident Indians, who are citizens of India holding an Indian passport and have temporarily immigrated to another country for six months or more for work, residence or any other purpose and who meet eligibility and admission requirements of FDDI can also apply for admission.</p>
						<p align="justify"> Only a person who is a NRI himself/herself may seek admission and no other candidate without NRI status would be eligible. Admission under this category shall be made on the basis of merit. If any or all seats from the NRI quota remain unfilled, the resulting vacancy shall be filled on merit of the applicants for admission.</p>
						<p align="justify"> The vacant seats under NRI / Industry-Sponsored/ Foreign Nationals category will not be offered to any regular candidate.</p>
						<p align="justify"> Candidates may apply for admission under NRI/ Industry Sponsored category in the mid of July 2023. Please visit the FDDI website for regular updates regarding submission of Application Form under this category.</p>
						<p align="justify"> <h2 style="font-size:14px;">ADMISSION FOR INTERNATIONAL STUDENTS</h2></p>
						<p align="justify"> Out of this 10% allocation, 5% seats are reserved for the foreigners/ NRI/ PIO. Further out of this, 5% Allocation of seats for the foreigners/NRI/PIO with maximum 2 seats per programme per campus will be considered for 25% scholarship/ concession in Tuition Fee under Study in India Programme. </p>
						<p align="justify"> The scholarship will be awarded in order of merit prepared on the basis of score achieved in TOEFL/ GRE/ GMAT/ SAT (as the case may be) by the students of above mentioned categories applying under ‘Study in India’ scheme of Government of India.</p>
						<p align="justify"> Foreign Nationals (whether residing in India or abroad) who meet the prescribed eligibility and admission requirements of FDDI can also apply for admission via <br> 

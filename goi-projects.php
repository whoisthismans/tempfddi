                            <h2 style="font-size:22px;">Integrated Development of Leather Sector (IDLS) Scheme</h2>
                            <p align="justify">The Integrated Development of Leather Sector (IDLS) scheme of the Government of India that is launced by DPIIT vide its Notification no. P-27015/4/2020-Leather Dated 03<sup>rd</sup> March 2022 aimed at enabling Tanneries, Footwear, Footwear components, Leather goods and accessories, Leather garments and Leather saddlery manufacturing units to upgrade themselves leading to Productive gains, Right-sizing of capacity, Cost cutting, Design and development including simultaneously encouraging entrepreneurs to diversify and set up new units in the areas as specified. The scheme is also aimed at enhancement of formal employment opportunity in one of the most employment intensive Leather and product sector. Since 2005 for the first time the scheme will be implemented through paperless system using this online portal with active interface of PIU, Applicant units and DPIIT.</p>
                            <p align="justify">FDDI and CLRI are the two arms of DPIIT working as Project Implementation Unit (PIU) for the Integrated Development of Leather Sector (IDLS) scheme for the period of 4 years (2022-26) for Tanneries and Product units respectively. The product units consists of Leather and Non-Leather footwear, Footwear Components, Leather goods, Leather garments, Saddlery and accessories. The quantum of assistance in this scheme has been raised to 15 crore for each unit and 30% of total investment is extended to all MSME units. To encourage domestic technology additional 5% grant will be awarded for all the machines manufactured within India. Additional 10% grant will be awarded for units situated at the North East region. The release of assistance to the units would be done by DPIIT.</p>

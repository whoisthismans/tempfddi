                            <h2 style="font-size:22px;color: #424770;">Integrated Development of Leather Sector (IDLS) Scheme</h2>
                            <p align="justify">The Integrated Development of Leather Sector (IDLS) scheme of the Government of India that is launced by DIPP vide its Notification no. 5/12/2016-Leather Dated 21st December 2017 aimed at enabling Tanneries, Footwear, Footwear components, Leather goods and accessories, Leather garments and Leather saddlery manufacturing units to upgrade themselves leading to Productive gains, Right-sizing of capacity, Cost cutting, Design and development including simultaneously encouraging entrepreneurs to diversify and set up new units in the areas as specified. The scheme is also aimed at enhancement of formal employment opprtunity in one of the most employment intensive Leather and product sector. Since 2005 for the first time the scheme will be iplemented through papeless system using this online portal with active interface of PIU, Applicant units and DIPP.</p>
                            <p align="justify">FDDI and CLRI are the two arms of DIPP working as Project Implementation Unit (PIU) for the Integrated Development of Leather Sector (IDLS) scheme for the period of 3 years (2017-20) for Tanneries and Product units respectiveley. The product units consists of Leather and Non-Leather footwear, Footwear Components, Leather goods, Leather garments, Saddlery and accessories. The quantum of assistance in this scheme has been raised to 3 crore and 30% of total invstment is extended to all MSME units. The release of assistance to the units would be done through through FDDI and CLRI for the respective units.</p>

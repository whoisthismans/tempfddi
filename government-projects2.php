                        <p align="justify">Based on the need more Industry specific courses in the area of Lasting, Finishing, Costing, Quality checking, Leather grading & Sorting, Repairing, Multi-skilling etc. will be provided as on-site services to the industry.</p>
                        <h3 style="font-size: 14px;font-weight: 700;color: #292929;margin: 0 0 15px;">Eligibility Criteria</h3>
                        <p align="justify">Following parameters will applicable in order to take the benefit of the program by the candidate:</p>
                        <ul class="campus-commen-ul project-imple-ul">
                            <li>The candidate shall have attended the age of 18yrs. at the time of joining the program having sound mind.</li>
                            <li>The candidate should have reading &writing capability, & aptitude for the training.</li>
                            <li>The candidate shall be unemployed at the time of joining the programme.</li>
                            <li>The participating industry can also select their own candidate meeting the above-mentioned criterion in consultation with the selection board. However under such condition the candidate after successful completion of the training has to join the sponsoring organization & will not be eligible for the open placement.</li>
                        </ul>
                        <h3 style="font-size: 14px;font-weight: 700;color: #292929;margin: 0 0 15px;">Selection Of The Trainees</h3>
                        <p align="justify">Selection of the trainees will be done through a screening committee, especially designed for the purpose and having the following members:</p>
                        <ul class="campus-commen-ul project-imple-ul">
                            <li>Center In charge of the respective training center</li>
                            <li>Representative of the local district administration.</li>
                            <li>Representative of the local industry</li>
                            <li>Representative from Department for Industrial Policy and Promotion, GoI</li>
                        </ul>
                        <h3 style="font-size: 14px;font-weight: 700;color: #292929;margin: 0 0 15px;">Assessment</h3>
                        <p align="justify">The assessment of the participant will be based upon the following:</p>
                        <ul class="campus-commen-ul project-imple-ul">
                            <li>The participant shall maintain a minimum attendance of 85% percent during the program.</li>
                            <li>Successful completion of the all units designed in the program.</li>
                            <li>The minimum level of grade shall be 60% in all the units for qualification.</li>
                        </ul>
                        <h3 style="font-size: 14px;font-weight: 700;color: #292929;margin: 0 0 15px;">Certification And Placement Of The Trainees</h3>
                        <ul class="campus-commen-ul project-imple-ul">
                            <li>Upon the successful completion of the training program the candidate will be awarded with the certificate of achievement.</li>
                            <li>The details of the successful trainees will also be displayed at the website www.fddiindia.com, along with their individual details.</li>
                            <li>Upon completion of the course the trainees will be placed in the footwear and leather industries of the vicinity, in association with the entrepreneurs industrialists of the sector.</li>
                        </ul>
                        <h3 style="font-size: 14px;font-weight: 700;color: #292929;margin: 0 0 15px;">Benefits Of The Programme</h3>
                        <p align="justify">The training program prepared for the youths, to help them to acquire skill & knowledge in the shoe making process.</p>
                        <p align="justify">During the training period the youths were exposed to the training facilities at the centers as well as in the industry. Trainees were informed about the international sizing and fitting standards. The youths gained knowledge on various sizing systems; it's conversion of one system to another, optimization in cutting of leather, on designing, on finishing, and ascertaining product prices with rationalized ways of costing.</p>
                        <p align="justify">Youths were very enthusiastic about the training. Handouts contains different aspects of footwear design, needle & its parts, different types of stitching machines, different types of stitching operations, leather & its parts (quality reasons), different types of lay outs were provided to the trainees. The handouts were specially prepared in Hindi with pictorial representation.</p>
                        <p align="justify">All the successful candidates are awarded certificates of achievements upon the completion of the training program.</p>
                        <p align="justify">The overall training has resulted in:</p>
                        <ul class="campus-commen-ul project-imple-ul">
                            <li>Improvement in the work practice.</li>
                            <li>Use of hand cutting method with the help of tin pattern leading to the saving of material.</li>
                            <li>Improvement in the quality of design & pattern cutting.</li>
                            <li>Improvement in the area of lasting & finishing process.</li>
                            <li>Knowledge enhancement about newer methods.</li>
                            <li>Trouble shooting in the manufacturing processes.</li>
                            <li>Employment generation</li>
                        </ul>
                        <p align="justify">FDDI's efforts in creating fresh & unemployed youths as skilled work force through an intensive training programme has been a great success and applauded at various forums as the most innovative and effective approach of implementation of any of the scheme carried out so far.</p>
                        <h3 style="font-size: 14px;font-weight: 700;color: #292929;margin: 0 0 15px;">Venue Of The Training</h3>
                        <p align="justify">The training will be provided through established training centers being set-up by FDDI in the major footwear and leather clusters of Kanpur & Agra in the initial phase. Apart from it, on site training will be provided for those industries that select their own trainees for general and specific training purposes.</p>

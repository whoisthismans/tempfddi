                        <h2 style="font-size:22px;color: #424770;">FDDI CENTERS</h2>
                        <p align="justify">FDDI is implementing the PSDP through establishment of Training centers at Agra, Kanpur, Bareilly, Delhi/NCR, Kolkata, Ranipet, Maharastra, Bihar, Chandigarh & various on-site location at the factory premises, where unemployed youths belonging from the economically weaker section of the society are being selected, trained and provided assistance to get employment in the footwear industry at the shop floor level operations.</p>
                        <p align="justify">FDDI has already provided trained, manpower to renowned footwear companies such as M/s. Prime Shoes Chennai, M/s. N. M. Zackriah & Company, Ambur, M/s. Dawar Shoes Agra, M/S. Arvind Footwear Kanpur, M/s. Rohit Surftrants Kanpur, M/s. Tej Shoes Agra, M/s. SSIPL Ltd., Sonepat, M/s. Super House, Unnao, M/s. Florence Shoes Co. Pvt. Ltd., Ambur, M/s. Leeyan Overseas, Kanpur, M/s. Tata International, Dewas, M/s. Zermatt Shoes, Ambur, M/s. Sarup Tanneries Ltd., Unnao, M/s. Tej Shoes Agra, M/S. Zeta Shoes, Rewari, Haryana, M/s. Good Leather Shoes, M/s. Oscar Leather Mohali, etc. The requirement to train the workers from other companies is also coming.</p>
                        <h3 style="font-size: 14px;font-weight: 700;color: #292929;margin: 0 0 15px;">Scope Of The Training</h3>
                        <p align="justify">The scope of the training program has been improved in quality and the increase in productivity with use of modern techniques, tool standardization, designs and methods. The areas of specialization of the training were mainly Cutting, Closing, Lasting and Finishing</p>
                        <p align="justify">The course curriculum of this training is as follows:</p>
                        <p align="justify">Cutting- Tin Pattern Making, Hand Cutting, Machine Cutting, Leather Inspection, Quality region of Hide and Side leather, Part of shoe, how to cut leather with quality- leather manipulation.</p>
                        <p align="justify">Closing- Types of Needles, Threads, Relation between thread and needle, Different types of stitching machines, Different types of stitching operations (seams), Upper making & minor machine maintenance.</p>
                        <h3 style="font-size: 14px;font-weight: 700;color: #292929;margin: 0 0 15px;">Training Material(S) Provided</h3>
                        <p align="justify">Relevant information in the form of booklet, use of machineries & Printed Manuals (the art of self learning) of FDDI, facilities of training center etc. were provided during the training program to the trainees.</p>
                        <p align="justify">The distribution of the knowledge was through the demonstration, organization of the group-based learning & through the visual presentation of the correct methods.</p>
                        <h3 style="font-size: 14px;font-weight: 700;color: #292929;margin: 0 0 15px;">Objective Of The Skill Development Programme</h3>
                        <p align="justify">The program has been formulated keeping in view the following major objectives:</p>
                        <ul class="campus-commen-ul project-imple-ul">
                            <li>Imparting basic skills on shop floor operations to unemployed youth through a demand driven Skill-Development Program.</li>
                            <li>Creation of adequate trained manpower to cope up with the requirement of Footwear/Leather industry.</li>
                            <li>Spread and sustain a technology and per person productivity culture in the Indian leather sector.</li>
                        </ul>
                        <h3 style="font-size: 14px;font-weight: 700;color: #292929;margin: 0 0 15px;">Trade In Which The Skills Are Proposed To Be Imparted</h3>
                        <ul class="campus-commen-ul project-imple-ul">
                            <li>Basic Clicking Operator Course</li>
                            <li>Basic Stitching Operator Course</li>
                        </ul>

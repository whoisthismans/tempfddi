                        <h2 style="font-size:16px;text-transform:capitalize;">How To Fill The Online Application Form</h2>
                        <p align="justify">Candidates are required to apply online through website: <a href="https://applyadmission.net/fddi2023" target="_blank">https://applyadmission.net/fddi2023</a> They can access this website also through the ‘Admissions 2023 - Apply Now’ banner on the homepage of official website of FDDI (<a href="https://www.fddiindia.com">www.fddiindia.com</a>)</p>
                        <p align="justify"><i>(Entries in the application form shall be required to be filled only in English.)  </i></p><br>
                    <h3 style="font-size:16px;text-transform:capitalize;">Submission of Application Form</h3>
                    <p align="justify"> Step By Step guidelines for applying online may be referred for the purpose. The prospectus is available at FDDI website <a href="https://www.fddiindia.com" target="_blank"> www.fddiindia.com </a> free of cost. The application fee has to be paid through online payment gateway only. Candidates have to pay this amount for Bachelor / Master Programme he / she applies for. 
					<br><br> Non-Refundable Application Fee for General/OBC (Non Creamy)/GEN-EWS Category: Rs. 600/- through online payment gateway only. <br><br> Non-Refundable Application Fee for SC/ ST/ PWD Category: Rs. 300/- through online payment gateway only.
					<br> <br>Mode of Payment of application fee: Credit Cards/Debit Cards/Internet banking. <br><br> 
					Payments of application fee (Rs.600/- or Rs.300/-) can be made online as explained above through any means. The bank and/or transaction charges on the application fee will be borne by the candidate. Candidates are advised to apply on or before the last date of submission of Application Form.
					<br><br> <b> Candidates are advised to keep a printout/photocopy of Application Form for further reference along with proof of payment. </b><br><!--Last Date to Apply online :15th April 2020. &nbsp-->

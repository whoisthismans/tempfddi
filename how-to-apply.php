<h3 style="font-size:16px;text-transform:capitalize;">Window to Edit/ Update Application Form</h3>
<p align="justify">Prior to making the payment candidate can edit/amend the information in the online application. Once the payment has been made by the candidate he/she will not be allowed to change/modify any information by the system. However, candidates will be given an opportunity to review and correct their personal information (changing the City of Examination is not permitted) in the application form from 26<sup>th</sup> May 2023 onwards till 27<sup>th</sup> May 2023 (16:00 Hrs.).<br> <br>Application fee already paid will not be refunded in case the application is found not eligible for admissions to FDDI after editing / updating of the application form. <br> <br>It is the sole responsibility of the candidate to provide correct information during the application process. At any stage if it is found that the information provided by the candidate is not true, FDDI has the right to cancel his/her candidature. Any direct or indirect attempt to influence the management and employees of the institute will lead to automatic disqualification of the candidate.
<br><br> After closing the window to edit the application form, the information filled up in the application form will not be changed at any stage. Therefore, candidates/guardians are advised to fill-up the application form with due diligence.</p>

						<p align="justify"> Leather Goods & Accessories Design Noida is organising  workshop for up-gradation of Skills and Training in Toy making through Institutions. </p>
						<p align="justify"> The Indian handicraft industry is facing enormous problems due to popularization of machine based low cost and superior quality consumer goods. With evolution of the modern market system economy, the artisans have lost their holds over the old patron-client market network. They are dependent on middle men like petty merchant capitalists who pay the artisans in wage on piece rate bases.  Sectional communities in India are recognized for their traditional skills, arts and crafts. But due to forces of competitive market and globalization, and also due to weakening of socioeconomic state of master craftsmen/artisan, these expertise are not being followed by the new generation. There is a necessity to boost traditional arts and entrepreneurial skill which are the pillar of cottage and small scale industry and create better market connections, enhance branding and ensure access to credit. </p>
						<p align="justify"> This workshop will be conducted to build capacity of master craftsmen/artisans and training of young generation through the master craftsmen/ artisans for traditional arts/crafts in leather toy making. This workshop of Toy making will be of 2 days duration whereby students will learn entire process of toy making-moulding to form structure, covering it with papier Mache to give proper shape and finally covering it with leather or fabrics. Students will be encouraged to use discarded fabrics and leather. Workshop will be taken by Mr. Mohammad Arshad Khan from Indore Toy cluster group. This workshop will be stepping stone towards revival of the traditional handicrafts. </p>
                        

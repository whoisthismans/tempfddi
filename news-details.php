                        <h3>FDDI Participates In Two-Day Conference On ‘Circular Economy, Recyclability And Sustainability Of The Rubber Industry’</h3>
                        <p>Footwear Design & Development Institute (FDDI) participated in two-day conference on ‘Circular Economy, Recyclability and Sustainability of the Rubber Industry’ which was held on 04th & 05th December 2019 at the LaLiT Great Eastern, Kolkata. The conference was organized by All India Rubber Industries Association (AIRIA), Eastern Region.</p>
                        <p>Kumar Sinha, IAS, Managing Director, FDDI was felicitated by AIRIA, Eastern Region. AIRIA is a not for profit making body serving the rubber industry and trade with the objectives of safeguarding and promoting interests of the industry. During the conference, a complete session on Footwear alongwith industry highlights and scopes in the region was conducted.</p>
                        <p>Delivering the ‘Keynote Address’, Mr. Arun Kumar Sinha, IAS, Managing Director, FDDI, said “From drones to satellite images and sensor technology, the rubber industry is poised for a radical change. On the manufacturing front, manufacturers of rubber products and rubber processors play a key role in the development of non-leather footwear too.”  </p>
                        <p>In his address, Mr. Sinha further said that “Considering the ever increasing role of non-leather footwear, of which rubber is one of the major constituent, FDDI is being empowered by the Ministry to bring up world class infrastructure and the skills second to none through upgradation of its Rohtak and Jodhpur campus into "Centres of Excellence (CoE),” on the thematic area of Center for Non-Leather Footwear, Products & Accessories and High Performance/ Specialized Footwear & Products and Start-Ups respectively.”</p>
                        <p>During the conference, a technical research presentation on ‘Technological Advancement Opportunity of Footwear & Foot Care Materials in India’ was given by Mr. Manoj Kumar Roy, Sr. Faculty – FDDI.</p>
                        <p>The conference was a perfect forum to discuss the new innovations taking place globally in the field of Rubber /Non-Leather Footwear Technology & allied fields. It played a transformative role in minimizing waste and making the most of resources by focusing on design thinking, systems, thinking product life extension and recycling.</p>

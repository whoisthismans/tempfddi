						<p align="justify">Ms. Renu Sharma, Consultant - Retail of FDDI, NOIDA campus presented a research paper titled ‘Shoppers’ Behaviour in Indian Shopping Streets- A model of Tangible & Intangible Factors Leading to Re-patronage Intention’ in Virtual Subsistence Marketplaces Conference which was held from 31st May 2020 to 1st June 2020.</p>
						

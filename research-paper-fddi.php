						<p align="justify">A ‘Workshop on Teaching Subsistence Marketplaces Online’ was conducted on the second day of conference. The workshop included simulation exercises, case studies, immersion interviews and project design. Furthermore, a panel on health and nutrition was organized with international experts from these sectors.</p>

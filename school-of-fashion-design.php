                                        <div class="course-aim-box">
                                            <p align="justify">This programme is specifically initiated with the intent of making the students to have a global knowledge of various fashion sectors so they are able to blend them with local cultures. It further aims to make the students equipped to enter the world of fashion by developing their own creative universe while being adaptable to different styles, types of brand and market segments at the same time and get benefit from our large array of networks within the industry. The programme fosters to create design professionals who understand dynamics of global business environment and meet the ever growing demand of the Design sector.</p>
                                        </div>
                                    </div>
                                    <div>
                                        <div class="course-aim-box">
                                            <p align="justify">It is a four years programme comprising of eight semesters focusing on inculcating a broad spectrum of design concepts and skills. Further, the design attitude of the students is developed through design thinking and  creative problem solving process.</p>
                                        </div>
                                    </div>
                                    <div>
                                        <div class="course-aim-box">
                                            <ul class="course-aim-ul">
                                                <li>
                                                    <h5>Design Foundation:</h5>
                                                    <p align="justify"> This being the first introductory module of the curriculum the emphasis is on opening the horizons of the individuals in order to build designing and professional competency. The students are taught basic skills in conceptualization and idea generation to support their designs. The subjects broadly taught during the foundation year are Design Evolution, Observational Drawing, 2D & 3D CAD, Tinkering Studio Design Thinking, Photography & Videography, etc.</p>
                                                </li>
                                                <li>
                                                    <h5>Specialization</h5>
                                                    <p align="justify">The Subsequent three years are devoted to promote the Technical expertise and Design excellence, introducing pupils to various Techno commercial aspects of the fashion industry and developing skills in the technical areas of garment manufacturing like Pattern Making, Garment Construction, Software Training in Computer Aided Design (Adobe Illustrator, CLO-3D etc.).</p>
                                                    <p align="justify">With our close associations to the industry the curriculum is designed to nurture creativity while providing a rigorous grounding in the practical and technical skills needed for career success, exposing the pupils to the real demands and practices of the fashion domain.</p>
                                                    <p align="justify">The students get intensive training & education pertaining to design thinking & innovation, Aesthetics in Design and Fashion, Creative Pattern Making, styling,  and organizing fashion shows. The students get ample opportunities to interact with buyers as well backstage with eminent designers learning the entire look and feel required for professional models for the ramp shows. One of the prime aspects of the curriculum is craft documentation where in the students travel to different part of the country and stay there with the craftsmen, to understand and learn our native crafts and accordingly develop innovative products to promote the Indian Craft. They also visit Production houses, Museums, Trade shows, printing and textile mills, and designer studios to enhance classroom studies.</p>
													<p align="justify"> We nurture the next generation of Fashion Entrepreneurs, Fashion Designers, Fashion Stylists, Make-up Artists, Fashion Photographers, Fashion Journalists and Fashion Event Managers. We give them the skills they need and provide them with unique insights into the workings of this ever-changing industry. </p>
										
												</li>
                                            </ul>

                                                <li>
                                                    <h5>Design Foundation:</h5>
                                                    <p align="justify">The first year of the four years programme emphasizes on opening the horizons of individuals towards design thinking. The two semesters’ rigorous training exposes the individuals to varied world of Fundamentals of Design and peripheral attitudes. The subjects broadly Orient towards are Observational Drawing, Design Thinking Process, Ideation and Innovation Technique, Material Studies, Essentials of management and Software skills.</p>
                                                </li>
                                                <li>
                                                    <h5>Core Design & Technnology Fundamentals:</h5>
                                                    <p align="justify">Here students understand the fundamental and the latest technical developments which govern the growth of the Leather, Lifestyle and Product Design sectors.</p>
													<p align="justify"> The modules orient the students towards Design Process techniques, Product development process, Product lifecycle approach, Idea and product Visualization (hand and software, 2D & 3D), Trend Forecasting, Machines Operations, Fashion history and culture, Design materials &Processes, Fashion Marketing and Merchandising, Human centered design, Graphic design, UI UXI etc. </p>
												</li>
                                                <li>
                                                    <h5>Specialization:</h5>
                                                    <img class="img-responsive" src="img/specilalization-lgad.JPG" alt="Specilalization">
													<p align="justify"> In the third year, students are using acquired skills and concepts to explore, design and develop products in different specialized area with hands on learning in the process. Emphasis is given to the usage of contemporary and futuristic principles and techniques in their design and development. Emphasis to create visual experience that aims to improve a design’s /product's aesthetic appeal and usability with suitable images, typography, space, layout and color. Students are encouraged to create interfaces that optimize user experience and drive conversion. </p>
													<p align="justify"><strong> The modules are designed with pool of courses leading to major in two different specializations: </strong>  </p>
													<p align="justify">1/Lifestyle product and Accessory Design orienting towards the development of Women’s and men’s wear, Leather and lifestyle goods  and accessory, Accessory design, business and retail gear, Home décor products, Outdoor gears, etc.  </p>
													<p align="justify">2/Visual Design specialization oriented towards Graphic design, User experience design, Human interface design, Electronic and visual interface development, etc.  </p>													
												</li>
												<li>
                                                    <h5>DESIGN PROJECTS, DESSERATATIONS AND INDUSTRY INTERNSHIPS:</h5>
                                                    <p align="justify">The last year focuses on building up industry, interdisciplinary research and management outlook of the students. The session focus on dissertations, creating Industry training and Internships, with inclusion of various projects and design development addressing customer, industry and social needs that helps to build up a sound portfolio and makes them industry ready.</p>
                                                </li>

                                            <p align="justify">The first year focuses on developing analytical tools and knowledge that supports the rest of the curriculum. First year program covers basic business functions (finance, marketing, and operations); management (research, and human resource); and fashion (trends, product knowledge) in which the retailers operate.</p>

                            <li>To upgrade specific skills and knowledge required for enhanced performance of expected duties and responsibilities;</li>
                            <li>To facilitate or enhance the development of a critical mass of trained human resources able to work or contribute effectively towards a specified sector.</li>                        
						</ul>												

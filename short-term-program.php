                        <h4>Overview </h4>
                        
						<p align="justify"> According to the report published by National Skill Development Committee (NSDC) on Human Resource & Skill Requirement in Leather and Leather Goods Sector, it is estimated that currently about 4.42 million people are employed in this sector.  Human workforce requirement in the leather and leather goods sector would reach to 6.81 million by the year 2022. Additional employment opportunities by the year 2022 would be of 2.39 million people.</p>
						<p align="justify"> Another report published by Council for Footwear Leather and Accessories (CFLA) states that the overall demand for footwear could reach up to 9 billion pairs by the year 2030 resulting as a major source of employment in the country. As per the report, to fill the employment gap industry needs mass training in the areas of cutting, stitching, lasting etc.</p>
						<p align="justify"> Keeping that in mind, short-term programs are designed to meet the manpower requirement and other specific needs and issues faced by the footwear and leather industry. They are conducted in a relatively short period of time. Leather & Leather products industries are employment-oriented industries, however, with the development of the technologies & integration of the worldwide market, there has been a shift in the business model of the organizations worldwide and also the demand for manpower has changed significantly. The human resource asset has also become critical as the growth of the sector has witnessed changes in the demand type.</p>
						<p align="justify"> The leather & leather product sector is labour oriented organization wherein the direct labour force comprises a major part of the employment. Therefore skills are equally paramount as knowledge.</p>
						<p align="justify"> However, on the higher end of the pyramid, techno-management & trade becomes a critical factor for the success of the industry therefore inculcation of the knowledge-based inputs are critical for the success of the industry.</p>
						
						<h2 style="font-size:14px;">Objectives of Short-Term Training </h2>
						<p align="justify"> Specifically, the aim of the short-term programs are:</p>
						

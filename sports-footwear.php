						<p align="justify">  An insightful seminar on the topic ‘Current Trends in Sports Footwear Manufacturing Industry’ was held at FDDI, Noida on 5th & 6th July 2023. </p>
						<p align="justify">  FDDI’s School of Footwear Design and Production (FDP) organized the seminar with the objective to upgrade the faculty and to make them aware with the current industry norms and standards. </p>
						<p align="justify">  The sessions were conducted by eminent speakers namely, Mr. Sreedhar Jayaraman, Mr. Tanmay Kr Mishra, Mr. S. D. Singh and Mr. S.M. Shahid Anwar, all of them having more than three decades of valuable experience in footwear manufacturing. </p>
						<p align="justify">  The first session was conducted by Mr. Sreedhar who gave a brief overview on how the sports footwear evolved over the ages and how each stage has contributed towards the development of the other. He also discussed about what technologies contributed to the development, the associated design features, the tangible and intangible factors associated in sports footwear.  </p>						
												

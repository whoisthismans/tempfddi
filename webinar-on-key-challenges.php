							<h3 align="center"> Webinar on ‘Key Challenges / Issues inhibiting India’s Footwear Growth Story’ organized by FDDI in association with FICCI </h3></br>

						<p align="justify"> As part of the virtual webinars titled ‘Walk the Talk’ series being organized by Footwear Design & Development Institute (FDDI) in association with Federation of Indian Chambers of Commerce & Industry (FICCI), another webinar on the topic ‘Key Challenges / Issues inhibiting India’s Footwear Growth Story’ was conducted on 1st June 2021.</p>
						<p align="justify"> Mr. Arun Kumar Sinha, IAS, Managing Director, FDDI and Mr. Dilip Chenoy, Secretary General, FICCI opened the session by briefing about the importance of the leather and footwear industry in India’s growth story and how it has been significantly affected by the COVID-19 pandemic since March 2020. </p>
																		
						<span class="news-details-img">
						<img class="img-responsive" src="img/new@fddi/AKSinhafddi.JPG" alt="img">
						</span>
						
						<p align="justify"> Eight industry leaders joined the roundtable for a high-level discussion which included the CEOs /leaders namely Ms. Namrata Chotrani – CEO Khadims, Mr. Ram Pingley - AVP, Aditya Birla Group, Mr. Adesh Gupta - CEO Liberty Shoes, Mr. Amit Ranchal – MD of Welcome Shoes, Dr. N Mohan – CEO - Clarks India, Mr. Nitesh Kumar - CEO, Reliance Footprints, Mr. Irshad Ahmed – MD of Farida Shoes, Mr. Karan Jain – MD of MB Rubber as panelist. The session was moderated by Mr. Mohit Bhasin, Partner, KPMG India. </p>
						<p align="justify"> The webinar provided opportunities to understand the challenges faced by leather and footwear industry of India and offered a platform to share and learn from other experiences for overall sectoral growth with focus on investments, trade and employment generation. </p>
						<p align="justify"> During the discussion, issues and challenges affecting the growth of the footwear industry in India were highlighted and the participants gave insights on how other countries like China, Vietnam, and Bangladesh have succeeded in growing their footwear production and exports hence, increasing their market share steadily.</p>
						<p align="justify"> It was also highlighted that non-leather footwear demand is growing globally and India needs to focus on building the capabilities in this space. One of the panelists also discussed on how the retail sector is booming and there is a great opportunity to increase domestic footwear consumption.</p>
						
						<span class="news-details-img">
						<img class="img-responsive" src="img/new@fddi/AKSinhafddi_wss.JPG" alt="img">
						</span>
						
						<p align="justify"> One of the key concern for these industry leaders was the implementation of BIS standards and how it can be a deterrent for the growth of fashion footwear segment. At the same time, the panel also agreed that there is a need to redefine the standards for health and safety footwear. The suggestions on overcoming the challenges included rationalization of import duties on key raw materials, promoting production of raw material for non-leather footwear in India, plug and play model at the cluster, Free Trade Agreement (FTA’s) with key markets, among others. </p>
						<p align="justify"> The entire panelist also emphasized that it is important for India to sync with the emerging global trends in order augment growth of the India footwear industry.</p>
						<p align="justify"> Thus, the webinar offered concise and insightful information on the challenges the Indian footwear industry is facing and what needs to be done to boost the overall growth of the sector. </p>
											
						
